function getOverTimeWorkingFat(job) {
  let jobs = job.list;
  let n = job.n;
  let result = 0;
  if (jobs.length <= 0) {
    new Error({
      error: 'Jobs to do have not been entered. Please enter Jobs'
    })
  } else if (jobs.length > 20000) {
    new Error({
      error: 'Too many Jobs have been entered.Please enter below 20000'
    })
  } else if (n > 1000000) {
    new Error({
      error: 'The value entered is too large. Please enter below 1000000'
    })
  } else {
    for (let i = 0; i < n; i++) {
      let MaxIndex = jobs.indexOf(jobs.reduce(max))
      if (jobs[MaxIndex] > 50000) {
        new Error({
          error: 'The value entered is too large. Please enter below 50000'
        })
      } else if (jobs[MaxIndex] > 0)
        jobs[MaxIndex]--;
      else {
        break;
      }
    }
    for (let i = 0; i < jobs.length; i++) {

      result = result + (jobs[i] * jobs[i]);
    }
    console.log(result);
    return result;
  }
}

function max(previous, current) {
  return previous > current ? previous : current;
};

